function grid(){
for(y = 0; y < maze.length; y++){
    
    for(x = 0; x < maze[y].length; x++){
    
        if(maze[y][x] == 0){
    ctx.fillStyle = "#00d639";
    ctx.fillRect(x*tSize,y*tSize,tSize,tSize);
}
        if(maze[y][x] == 2){
    ctx.drawImage(victory, x*tSize,y*tSize,tSize,tSize);
}
        if(maze[y][x] == 1){
    ctx.drawImage(matrix, x*tSize,y*tSize,tSize,tSize);
}
        if(maze[y][x] == 3){
    ctx.drawImage(smithBomb, x*tSize,y*tSize,tSize,tSize);
}
        if(maze[y][x] == player){
    ctx.drawImage(neo, x*tSize,y*tSize,tSize,tSize);
    playerPosition = {y:y,x:x};
            }
        }
    }
}

window.addEventListener('keydown', function(event){
    console.log(event.keyCode);

switch (event.keyCode) {
    case 38: //KeyUp
        keyUp();
        grid();
        break;

    case 39: //KeyRight
        keyRight();
        grid();
        
        break;
    case 40: //KeyDown
        keyDown();
        grid(); 
        break;

    case 37: //KeyLeft
        keyLeft();
        grid(); 
        break;

    default:
        break;
}

})

